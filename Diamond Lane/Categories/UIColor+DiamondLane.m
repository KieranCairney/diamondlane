#import "UIColor+DiamondLane.h"

@implementation UIColor (DiamondLane)

+(UIColor *)dml_tintColor {
    
    return [UIColor colorWithRed:10/255.0 green:145/255.0 blue:171/255.0 alpha:1.0];
    
}

+(UIColor *)dml_grayColor {
    
    return [UIColor colorWithRed:0.6 green:0.6 blue:0.6 alpha:1.0];
    
}

+(UIColor *)dml_lightGrayColor {
    
    return [UIColor colorWithRed:0.8 green:0.8 blue:0.8 alpha:1.0];
    
}

@end
